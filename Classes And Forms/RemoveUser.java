    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // display user info
        int place = jList1.getSelectedIndex();
        User x = Toolkit.getUser(place);
        jLabel8.setText(x.getFirst());
        jLabel9.setText(x.getLast());
        jLabel10.setText(x.getLocation());
        if (x.getIsNewHire()) jLabel11.setText("Yes");
        else jLabel11.setText("No");
        SimpleDateFormat sdf = new SimpleDateFormat("E MMM d y");
        //jLabel12.setText(x.getStartDate().toString());
        jLabel12.setText(sdf.format(x.getStartDate()));

        //display current computer info
        if (!x.missingComputer()) {
            jLabel18.setText(x.getCurrentComputer().getModel());
            jLabel19.setText(x.getCurrentComputer().getAssetTag());
            jLabel20.setText(x.getCurrentComputer().getSerial());
            jLabel21.setText(x.getCurrentComputer().getName());
        }
        else {
            jLabel18.setText("N/A");
            jLabel19.setText("N/A");
            jLabel20.setText("N/A");
            jLabel21.setText("N/A");
        }
    }//GEN-LAST:event_jList1ValueChanged


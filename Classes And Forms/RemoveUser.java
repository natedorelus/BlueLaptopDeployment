    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jList1.isSelectionEmpty()) JOptionPane.showMessageDialog(rootPane, "You must first select a user.", "No User Selected", JOptionPane.WARNING_MESSAGE);
        else {
            String name = jList1.getSelectedValue();
            Toolkit.remove(jList1.getSelectedIndex());
            super.dispose();
            int choice = JOptionPane.showConfirmDialog(rootPane, name + " has been successfully removed. Would you like to remove another user?", "User Successfully Removed", JOptionPane.YES_NO_OPTION);
            if (choice == 0) {
                if (Toolkit.isEmpty()) JOptionPane.showMessageDialog(rootPane, "There aren't any users left to remove.", "No Users Left", JOptionPane.WARNING_MESSAGE);
                else {
                    RemoveUser remove = new RemoveUser();
                    remove.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

